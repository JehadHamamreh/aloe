% Coding based on bit repetition

function out = repetition_coding(input, rep)

    D = length(input);
    S = rep*D;
    out = zeros(1,S);
    
    for i=0:D-1;
        for j=1:rep
            out(i*rep+j) = input(i+1);
        end
    end

end